# Hyperparameters optimized for Sindhi
batch_size = 32  
block_size = 128  
max_iters = 8000  
eval_interval = 500
learning_rate = 2e-4  
device = 'cuda' if torch.cuda.is_available() else 'cpu'
eval_iters = 200
n_embd = 384
n_head = 6
n_layer = 6
dropout = 0.2


torch.manual_seed(1337)

# Load and preprocess Sindhi text
def load_sindhi_text(file_path):
    # Try different encodings for Sindhi
    encodings = ['utf-8', 'utf-16', 'windows-1256', 'iso-8859-6']
    for encoding in encodings:
        try:
            with open(file_path, 'r', encoding=encoding) as f:
                text = f.read()
            print(f"Successfully read with {encoding} encoding")
            return text
        except UnicodeDecodeError:
            continue
    raise ValueError("Failed to decode file with tried encodings")

def preprocess_sindhi(text):
    # Keep only Sindhi/Arabic script characters and basic punctuation
    text = re.sub(r'[^\u0600-\u06FF\u0750-\u077F\uFB50-\uFDFF\uFE70-\uFEFF\s\.،؛؟!]', '', text)
    # Normalize whitespace
    text = re.sub(r'\s+', ' ', text).strip()
    return text

# Load and clean the text
raw_text = load_sindhi_text('/content/only_articles.csv')  # Update path
text = preprocess_sindhi(raw_text)

# Create vocabulary
chars = sorted(list(set(text)))
vocab_size = len(chars)
print(f"Vocabulary size: {vocab_size}")
